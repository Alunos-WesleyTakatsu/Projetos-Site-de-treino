var resultado;

function mensagem () {
    var numero1 = parseInt(prompt("Digite um número","5"));
    var numero2 = parseInt(prompt("Digite outro número","10"));
    var numero3 = parseInt(prompt("Digite mais um número","15"));
    resultado = numero1 + numero2 + numero3;
    alert("O resultado da conta é " + resultado);
}

